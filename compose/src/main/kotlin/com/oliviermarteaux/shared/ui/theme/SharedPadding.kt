package com.oliviermarteaux.shared.ui.theme

import androidx.compose.ui.unit.Dp
import androidx.compose.ui.unit.dp

/**
 * Shared reusable padding values.
 */
object SharedPadding {

    /** Extra-small padding = **4.dp** */
    val xxs: Dp = 4.dp

    /** Small padding = **8.dp** */
    val xs: Dp = 8.dp

    /** Medium padding = **12.dp** */
    val small: Dp = 12.dp

    /** Large padding = **16.dp** */
    val medium: Dp = 16.dp

    /** Extra-large padding = **24.dp** */
    val large: Dp = 24.dp

    /** Extra-large padding = **24.dp** */
    val xl: Dp = 32.dp

    /** Double-extra-large padding = **48.dp** */
    val xxl: Dp = 48.dp
}